import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

const systemPrompt = `You are Medi-Share Assistant, a helpful AI guide for the Medi-Share platform - a healthcare marketplace for buying and selling unused medicines at 50% of original price.

Key information about Medi-Share:
- Users can list unused medicines (must have >6 months before expiry)
- Categories include: Blood Pressure, Diabetes, Cancer, Pain Relief, Antibiotics, Heart, Respiratory, Vitamins
- Sellers can upload medicine photos and bills for verification
- Location-based search within 5-10km radius
- Platform takes 10-15% commission on sales
- Donation option available for those who want to give medicines for free
- In-app messaging for buyer-seller coordination

How to use the platform:
1. **Browse Medicines**: Use the Search page to find medicines by name, category, or location
2. **List a Medicine**: Click "List Medicine" to sell your unused medicines - add photos, expiry date, price (50% of original), and your location
3. **Contact Sellers**: Use the messaging feature to coordinate pickup/delivery
4. **Dashboard**: View your listings, messages, and profile settings

Be friendly, concise, and helpful. Guide users step-by-step when they need help.`;

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');
    
    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: 'Rate limit exceeded. Please try again later.' }), {
          status: 429,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: 'Service temporarily unavailable.' }), {
          status: 402,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        });
      }
      const errorText = await response.text();
      console.error('AI gateway error:', response.status, errorText);
      throw new Error('AI gateway error');
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, 'Content-Type': 'text/event-stream' },
    });
  } catch (error) {
    console.error('Chat assistant error:', error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : 'Unknown error' }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
